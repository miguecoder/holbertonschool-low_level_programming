#include <stdlib.h>
#include <stdio.h>
#include <time.h>

/**
 * main - print -> print the last digit of the number stored in the variable n.
 *
 * Return: Always 0 (Success)
 */

int main(void)

{
	char letra;

	for (letra = 'z'; letra >= 'a'; letra--)
		putchar (letra);
	putchar('\n');
	return (0);
}

